class problem
{
public:
    problem(const vector<string> &_input) : input(_input)
    {
        cout << "Size of Input: " << input.size() << endl;

        for (auto e : input)
            game.push_back(pair<char, char>(e[0], e[2]));
    }

    //                Wins against
    // A: Rock            C
    // B: Paper           A
    // C: Scissors        B
    //
    // Round Score:
    // A: 1
    // B: 2
    // C: 3
    // Lost: 0
    // Draw: 3
    // Win : 6

    unsigned long playgame(char elf, char me)
    {
        unsigned long retval = 0;

        // My reward
        retval += me - 'A' + 1;

        // Game reward
        if (elf == me) // Draw
            retval += 3;
        if (me == elf + 1 || (me == 'A' && elf == 'C')) // Won
            retval += 6;

        return retval;
    }

    unsigned long playgamesA(char xreplace, char yreplace, char zreplace)
    {
        unsigned long retval = 0;

        for (auto g : game)
        {
            char elf = g.first;
            char me = ('X' == g.second ? xreplace : ('Y' == g.second ? yreplace : zreplace));

            retval += playgame(elf, me);
        }

        return retval;
    }

    unsigned long playgamesB()
    {
        // X: I need to loose
        // Y: I need to draw
        // Z: I need to win
        unsigned long retval = 0;

        for (auto g : game)
        {
            char elf = g.first;
            char me = ('X' == g.second ? elf - 1 : ('Y' == g.second ? elf : elf + 1));

            if (me < 'A')
                me = 'C';
            if (me > 'C')
                me = 'A';

            retval += playgame(elf, me);
        }

        return retval;
    }

    unsigned long playalliterations()
    {
        unsigned long retval = 0;

        // possible iterations:
        for (char x = 'A'; x <= 'C'; ++x)
            for (char y = 'A'; y <= 'C'; ++y)
                for (char z = 'A'; z <= 'C'; ++z)
                    if (x != y && x != z && y != z)
                    {
                        unsigned long gameresult = playgamesA(x, y, z);
                        if (gameresult > retval)
                            retval = gameresult;
                    }

        return retval;
    }

private:
    const vector<string> input;
    vector<pair<char, char>> game;
};

